Student Feedback System
The Student Feedback System is a tool for evaluating and analyzing the performance of faculty. This type of Student Feedback system eliminates the time-consuming task of physically inspecting each student's feedback pages. Another essential characteristic of the SFS is that neither the administrator nor the student must be physically present to give or assess feedback. Admin, Faculty, and Students are the three users.

<br>•	Admin can add new faculty, View faculty list, Update faculty data, Delete faculty.</br>

<br>•	Faculty can login, update their profile, check feedback given by students.</br>

<br>•	Student can Register, Login, view profile, update profile, choose faculty and give feedback.</br>
